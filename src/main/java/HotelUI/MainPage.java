    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed

        if (jTabbedPane2.getSelectedIndex() == 0) {
            if ((txtCardName.getText().trim().isEmpty()) || (txtCardDate.getText().trim().isEmpty()) || (txtCardCVV.getText().trim().isEmpty()) || (txtCardNumber.getText().trim().isEmpty())) {
                JOptionPane.showMessageDialog(null, "Please fill up card detail" , "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                bookingID = Booking.autoIncrement();
                invoiceID = Invoice.autoIncrement();
                Booking.addBooking(new Booking(bookingID, selectRoom, dateIn, dateOut, dayOfStay, guestID, totalAmount));
                Guest.addGuest(new Guest(guestID, guestName, IC, nationality, email, contactNumber));
                Invoice.addInvoice(new Invoice(invoiceID, (new Date().toInstant().atZone(ZoneId.systemDefault()).toLocalDate()), bookingID));
                setInvoice();
                pnlAddBooking.removeAll();
                pnlAddBooking.add(pnlInvoicePage);
                pnlAddBooking.revalidate();
                pnlAddBooking.repaint();
            }
        } else if (jTabbedPane2.getSelectedIndex() == 1) {
            if (txtAmountChange1.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please insert paid amount!" , "Error", JOptionPane.ERROR_MESSAGE);
            } else  if (!txtAmountChange1.getText().trim().isEmpty()) {
                double change = Double.parseDouble(txtAmountChange1.getText());
                if (change <= 0) {
                    bookingID = Booking.autoIncrement();
                    invoiceID = Invoice.autoIncrement();
                    Booking.addBooking(new Booking(bookingID, selectRoom, dateIn, dateOut, dayOfStay, guestID, totalAmount));
                    Guest.addGuest(new Guest(guestID, guestName, IC, nationality, email, contactNumber));
                    Invoice.addInvoice(new Invoice(invoiceID, (new Date().toInstant().atZone(ZoneId.systemDefault()).toLocalDate()), bookingID));
                    setInvoice();
                    pnlAddBooking.removeAll();
                    pnlAddBooking.add(pnlInvoicePage);
                    pnlAddBooking.revalidate();
                    pnlAddBooking.repaint();
                } else {
                    JOptionPane.showMessageDialog(null, "Please pay enough!" , "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btnConfirmActionPerformed

