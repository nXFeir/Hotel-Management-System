    private void totalAmount() {
        double cleaning, tourism, service, dayfee;
        dayfee = this.dayOfStay * 350;
        cleaning = dayfee * 0.05;
        tourism = this.dayOfStay * 10;
        service = dayfee * 0.1;
        totalAmount = dayfee + cleaning + tourism + service;
        
        lblNight.setText("RM350 x " + this.dayOfStay + "nights");
        lblNightPrice.setText("RM" + dayfee);
        lblCleaning.setText("RM" + cleaning);
        lblService.setText("RM" + service);
        lblTourism.setText("RM" + tourism);
        lblTotalAmount.setText("RM" + totalAmount);
        txtTotalAmount1.setText("" + totalAmount);
    }
    
