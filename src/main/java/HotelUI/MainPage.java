    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        if (dtcIn.getDate() != null && dtcOut.getDate() != null) {
            dateIn = dtcIn.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
            
            dateOut = dtcOut.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();    
            dtcIn.setEnabled(false);
            dtcOut.setEnabled(false);
            
            ArrayList<String> availableRoom = Booking.searchRoom(dateIn, dateOut);
            
            outerloop :
            for (JButton b : getJButton()) {
                for (int i = 0; i < availableRoom.size(); i++) {
                    if (b.getText().substring(5).equals(availableRoom.get(i))) {
                        b.setEnabled(true);
                        continue outerloop;
                    } 
                }
            } 

        } else {
            JOptionPane.showMessageDialog(null, "Please choose a date for Date In and Date Out", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

